# Дан список чисел. Выделить среди них числа, удовлетворяющие условию: 
# следующее больше предыдущего. 
# Пример: [1, 5, 2, 3, 4, 6, 1, 7] => [1, 2, 3] или [1, 7] или [1, 6, 7] и т.д.
# a =  [int(i) for i in input().split()]
a = [1, 5, 2, 3, 4, 6, 2, 3]
def Form_List(a):
    b = [ ]
    for i in range(1,len(a)):
        if a[i]>a[i-1]:
            b.append(a[i])
    return b  
print(Form_List(a))